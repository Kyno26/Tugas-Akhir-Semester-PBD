    private void hitungBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungBtnActionPerformed
        try{
            String str = txt_dibeli.getText();
            if (str.length()==0) {
                JOptionPane.showMessageDialog(this,"Isi jumlah yang dibeli terlebih dahulu!","Notification",JOptionPane.WARNING_MESSAGE);
            }else{
                String sold = txt_stockSold.getText();
                int terjual = Integer.parseInt(sold);
                
                String avai = txt_stockAva.getText();
                int tersedia = Integer.parseInt(avai);
                
                String beli, bought, sisa;
                int buy, soldED, leftOver, tersisa;
                
                //stok terjual
                beli = txt_dibeli.getText();
                buy = Integer.parseInt(beli);
                soldED = terjual + buy;
                
                //stok tersisa
                beli = txt_dibeli.getText();
                leftOver = Integer.parseInt(beli);
                tersisa = tersedia - leftOver;

                bought = String.valueOf(soldED);
                txt_stockSold.setText(bought);
                sisa = String.valueOf(tersisa);
                txt_stockAva.setText(sisa);
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Inputan harus angka!","Notification",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_hitungBtnActionPerformed

