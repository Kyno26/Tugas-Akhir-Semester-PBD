    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        kadaluarsaBox.setDateFormatString("dd/MM/yyyy");
        
        if (TableProduct.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this,"Tabel Kosong!","Notification",JOptionPane.WARNING_MESSAGE);
        }else if(TableProduct.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this,"Pilih Data yang Akan Diubah!","Notification",JOptionPane.WARNING_MESSAGE);
        } else{
            String id_produk = model.getValueAt(TableProduct.getSelectedRow(),0).toString();
            String nama = txt_nama.getText();
            String strdate = dateFormat.format(kadaluarsaBox.getDate());
            String totstock = txt_totalStock.getText();
            int jumlahstock = Integer.parseInt(totstock);
            String sold = txt_stockSold.getText();
            int terjual = Integer.parseInt(sold);
            String avai = txt_stockAva.getText();
            int tersedia = Integer.parseInt(avai);

            String sql = "UPDATE product SET product_name=?,total_stock=?,stock=?,sold_stock=?,expiry_date=? WHERE id_product =?";
            try{
                PreparedStatement statement = (PreparedStatement)conn.prepareStatement(sql);
                statement.setString(1, nama);
                statement.setInt(2, jumlahstock);
                statement.setInt(3, Integer.parseInt(txt_stockAva.getText()));
                statement.setInt(4, Integer.parseInt(txt_stockSold.getText()));
                statement.setString(5, strdate);
                statement.setString(6, id_produk);
                
                statement.executeUpdate();
                reset();
                tampil();
                JOptionPane.showMessageDialog(this,"Berhasil Update!","Success",JOptionPane.PLAIN_MESSAGE);
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Gagal Update! "+e,"Failed",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

