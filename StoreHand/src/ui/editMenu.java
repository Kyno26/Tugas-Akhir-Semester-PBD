    private void tampil(){
            try{
                model.getDataVector().removeAllElements();
                model.fireTableDataChanged();
                String query = "SELECT * FROM product WHERE product.id_product";
                Statement statement = (Statement) conn.createStatement();
                ResultSet result = statement.executeQuery(query);
                if (!result.next()) {
                    JOptionPane.showMessageDialog(this,"Data Kosong!","Notification",JOptionPane.WARNING_MESSAGE);
                }else{
                    while(result.next()){
                        Object[] o = new Object[6];
                        o[0] = result.getString("id_product");
                        o[1] = result.getString("product_name");
                        o[2] = result.getInt("total_stock");
                        o[3] = result.getInt("stock");
                        o[4] = result.getInt("sold_stock");
                        o[5] = result.getString("expiry_date");
                        
                        model.addRow(o);
                    }
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(this, ex);
            }
    }
    
